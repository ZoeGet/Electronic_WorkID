void DisplayChinese_DrawChar16x16(uint16_t x, uint16_t y, const unsigned char font[DISPLAY_CHINESE_BYTES], uint16_t color, uint16_t bgColor)
{
    uint8_t row;
    uint8_t col;

    if (font == NULL) {
        return;
    }

    for (col = 0U; col < DISPLAY_CHINESE_WIDTH; col++) {
        uint16_t colData = ((uint16_t)font[col * 2U] << 8) | font[col * 2U + 1U];
        for (row = 0U; row < DISPLAY_CHINESE_HEIGHT; row++) {
            uint16_t pixelColor = ((colData & (0x8000U >> row)) != 0U) ? color : bgColor;
            ST7735_FillRect((uint16_t)(x + col), (uint16_t)(y + row), 1U, 1U, pixelColor);
        }
    }
}

void DisplayChinese_DrawWorkCardTitle(uint16_t x, uint16_t y, uint16_t color, uint16_t bgColor)
{
    uint8_t index;

    for (index = 0U; index < DISPLAY_CHINESE_COUNT; index++) {
        DisplayChinese_DrawChar16x16((uint16_t)(x + index * DISPLAY_CHINESE_WIDTH), y, ling[index], color, bgColor);
    }
}
